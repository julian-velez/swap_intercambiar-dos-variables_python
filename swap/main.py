# Se asignan valores iniciales a las variables
# a tiene el valor 100 y b tiene el valor 200
a, b = 100, 200

# En esta línea ocurre un intercambio de valores:
# a toma el valor de b (200) y b toma el valor de a (100)
# Esto se logra gracias a la asignación múltiple en Python
a, b = b, a

# Se imprime el nuevo valor de a (que ahora es 200)
print("a:", a)

# Se imprime el nuevo valor de b (que ahora es 100)
print("b:", b)
